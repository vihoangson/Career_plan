					<div class="post-content">
						<h2>LoD &#8211; Law of Demeter</h2>
<p><a href="https://en.wikipedia.org/wiki/Law_of_Demeter" target="_blank">https://en.wikipedia.org/wiki/Law_of_Demeter</a></p>
<p>nguyên lý này còn có một tên gọi khác là <b>principle of least knowledge </b>(nguyên lý kiến thức tối thiểu) là một trường hợp đặc biệt của nguyên lý <strong>loose coupling</strong> (khớp nối lỏng lẻo) .</p>
<p>nội dung chính của nguyên lý là nhấn mạnh sự cô lập trong phạm vi của unit (ở đây có thể hiểu là class), mỗi unit chỉ nên hạn chế sự hiểu biết (phạm vi truy cập) bên trong chính nó, cụ thể một class và các method của class có thể truy xuất các thuộc tính, phương thức của chính nó và chỉ nên truy xuất các thuộc tính và phương thức của các class khác bằng tham số truyền vào thay vì khởi tạo đối tượng của class khác bên trong class.</p>
<p>nguyên tắc này mang lại nhiều lợi ích trong việc xây dựng các unit test.</p>
<h2>DRY &#8211; Don&#8217;t Repeat YourSelf</h2>
<p><a href="https://en.wikipedia.org/wiki/Don%27t_repeat_yourself" target="_blank">https://en.wikipedia.org/wiki/Don%27t_repeat_yourself</a></p>
<p>nguyên lý về tránh sự lặp lại của tất cả các loại thông tin, trong kiến trúc đa tầng thì điều này cực kỳ hữu ích, nó mang lại cho phần mềm kiến trúc rõ ràng và dễ bảo trì.</p>
<h2>KISS &#8211; Keep It Simple Stupid</h2>
<p><a href="https://en.wikipedia.org/wiki/KISS_principle" target="_blank">https://en.wikipedia.org/wiki/KISS_principle</a></p>
<p>nguyên lý này chỉ ra rằng <strong>sự đơn giản</strong> của một hệ thống sẽ mang lại hiệu quả hơn so với một hệ thống phức tạp. bởi vậy <strong>sự đơn giản</strong> là chính là chìa khóa trong việc thiết kế phần mềm và sự phức tạp là điều cần nên tránh.</p>
<h2>YAGNI &#8211; You aren&#8217;t gonna need it</h2>
<p>nguyên lý nhấn mạnh việc tránh xây dựng các chức năng dưa thừa khi mà chúng chưa thực sự cần thiết. như Ron Jeffries đã viết : &#8220;Always implement things when you actually need them, never when you just foresee that you need them.&#8221; hãy chỉ luôn luôn thực hiện những thứ khi mà bạn thực sự cần chúng, không bao giờ , không bao giờ trước khi bạn cảm thấy cần nó.</p>
<h2>SOLID &#8211; Single responsibility, Open-closed, Liskov substitution, Interface segregation and Dependency inversion</h2>
<p>là tập hợp của nhiều nguyên lý &#8230; tới đây thì để hiểu được cụ thể thì mọi người hãy tiếp tục tìm hiểu đi nhé 😀</p>
<p><a href="https://en.wikipedia.org/wiki/Category:Programming_principles">https://en.wikipedia.org/wiki/Category:Programming_principles</a></p>
